			<h3>User Security</h3>
<p align="justify"><strong>Authentication:</strong> User data on our database is logically segregated by account-based access rules. User accounts 
have unique usernames and passwords that must be entered each time a user logs on. OneRays issues a session cookie only to record 
encrypted authentication information for the duration of a specific session. The session cookie does not include the password of the user.
<br>
<strong>Passwords:</strong> User application passwords have minimum complexity requirements. Passwords are individually salted and hashed.
<br>
<strong>Single Sign-On:</strong> For our Team Collaboration accounts, OneRays supports SAML 2.0 integration, which allows you to control access to 
OneRays across your organization and define authentication policies for increased security.
<br>
<strong>Data Encryption:</strong> Certain sensitive user data, such as credit card details and account passwords, are stored in encrypted format.
<br>
<strong>Data Portability:</strong> OneRays enables you to export your data from our system in a variety of formats so that you can back it up, or
 use it with other applications.<br>
<strong>Privacy:</strong> We have a comprehensive privacy policy that provides a very transparent view of how we handle your data, including how we 
use your data, who we share it with, and how long we retain it.<br></p>

<h3>Physical Security</h3>
<p align="justify">All OneRays information systems and infrastructure are hosted in world-class data centers. These data centers include 
all the necessary physical security controls you would expect in a data center these days (e.g., 24×7 monitoring, cameras, visitor logs, entry 
requirements). OneRays has dedicated cages to separate our equipment from other tenants. In addition, these data centers are SOC 2 accredited. 
For more information, visit SuperNAP and InterNAP. If you are looking for FluidSurvey or FluidReview information, please contact us directly.
</p>

<h3>Availability</h3>
<p align="justify">
<strong>Connectivity:</strong> Fully redundant IP network connections with multiple independent connections to a range of Tier 1 Internet access providers.
<br><strong>Power:</strong> Servers have redundant internal and external power supplies. Data centers have backup power supplies, and are able to draw power from the multiple substations on the grid, several diesel generators, and backup batteries.
<br><strong>Uptime:</strong> Continuous uptime monitoring, with immediate escalation to OneRays staff for any downtime.
<br><strong>Failover:</strong> Our database is replicated in real-time and can failover in less than an hour.
<br><strong>Backup Frequency:</strong> Backups occur daily at multiple geographically disparate sites.</p>

<h3>Network Security</h3>
<p align="justify"><strong>Access Control:</strong> Secure VPN, 2FA (two-factor authentication), and role-based access is enforced for systems management by authorized engineering staff.
<br><strong>Logging and Auditing:</strong> Central logging systems capture and archive all internal systems access including any failed authentication attempts.
<br><strong>Encryption in Transit:</strong> By default, our Ad's collectors have Transport Layer Security (TLS) enabled to encrypt respondent traffic. All other 
communications with the onerays.com website are sent over TLS connections, which protects communications by using both server authentication
 and data encryption. This ensures that user data in transit is safe, secure, and available only to intended recipients. Our application 
 endpoints are TLS only and score an “A” rating on SSL Labs‘ tests. We also employ Forward Secrecy and only support strong ciphers for 
 added privacy and security.</p>
<h3>Vulnerability Management</h3>
<p align="justify">
<strong>Patching:</strong> Latest security patches are applied to all operating systems, applications, and network infrastructure to mitigate exposure to vulnerabilities.
<br><strong>Third Party Scans:</strong> Our environments are continuously scanned using best of breed security tools. These tools are configured to perform application and network vulnerability assessments, which test for patch status and basic misconfigurations of systems and sites.
<br><strong>Penetration Testing:</strong> External organizations perform penetration tests at least annually.
<br><strong>Bug Bounty:</strong> We take the security of our platforms very seriously! OneRays runs a private bug bounty program to ensure our applications are
 continuously reviewed for vulnerabilities.</p>
<h3>Organizational & Administrative Security</h3>
<p align="justify">
<strong>Information Security Policies:</strong> We maintain internal information security policies, including incident response plans, and regularly review and update them.
<br><strong>Employee Screening:</strong> We perform background screening on all employees, to the extent possible within local laws.
<br><strong>Training:</strong> We provide security and technology use training for employees.
<br><strong>Service Providers:</strong> We screen our service providers and bind them under contract to appropriate confidentiality and security obligations if they deal with any user data.
<br><strong>Access:</strong> Access controls to sensitive data in our databases, systems, and environments are set on a need-to-know / least privilege necessary basis.
<br><strong>Audit Logging:</strong> We maintain and monitor audit logs on our services and systems.</p>

<h3>Software Development Practices</h3>
<p align="justify">
<strong>Stack:</strong> We code in Python and run on SQL Server, Windows, and Ubuntu.
<br><strong>Coding Practices:</strong> Our engineers use best practices and industry-standard secure coding guidelines which align with the OWASP Top 10.
<br><strong>Deployment:</strong> We deploy code dozens of times during the week, giving us the ability to react quickly in the event a bug or vulnerability is
 discovered within our code.</p>
<h3>Compliance and Certifications</h3>

<p align="justify">Despite best efforts, no method of transmission over the Internet and no method of electronic storage is perfectly secure. 
We cannot guarantee absolute security. However, if OneRays learns of a security breach, we will notify affected users so that they can take 
appropriate protective steps. Our breach notification procedures are consistent with our obligations under various state and federal laws 
and regulation, as well as any industry rules or standards that we adhere to. Notification procedures include providing email notices or 
posting a notice on our website if a breach occurs.</p>
<h3>Your Responsibilities</h3>
<p align="justify">Keeping your data secure also depends on you ensuring that you maintain the security of your account by using sufficiently 
complicated passwords and storing them safely. You should also ensure that you have sufficient security on your own systems, to keep any Ad's 
data you download to your own computer away from prying eyes. We offer TLS to secure the transmission of Ad's responses, but it is your 
responsibility to ensure that your Ad's are configured to use that feature where appropriate. For more information on securing your Ad'ss,
 visit our Help Center.</p>
